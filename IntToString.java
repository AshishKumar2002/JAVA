package ifElse;

import java.util.Scanner;

public class IntToString {
	
	public static void main(String[] args) {
		Scanner sc= new Scanner(System.in);
		int n=sc.nextInt();
		if(n==1) {
			System.out.println("One");
		}
		if(n==2) {
			System.out.println("Two");
		}
		if(n==3) {
			System.out.println("Three");
		}
		
	}

}
